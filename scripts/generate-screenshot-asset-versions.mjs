import { createHash } from "node:crypto"
import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..")
const publicRoot = path.join(root, "public")
const screenshotsRoot = path.join(publicRoot, "model-screenshots")
const outputFile = path.join(root, "src", "data", "screenshot-asset-versions.ts")
const versions = {}

function walk(directory) {
  for (const entry of readdirSync(directory, { withFileTypes: true })) {
    const filePath = path.join(directory, entry.name)

    if (entry.isDirectory()) {
      walk(filePath)
    } else if (entry.isFile() && entry.name === "desktop.webp") {
      const publicPath = `/${path.relative(publicRoot, filePath).split(path.sep).join("/")}`
      versions[publicPath] = createHash("sha256").update(readFileSync(filePath)).digest("hex").slice(0, 12)
    }
  }
}

if (existsSync(screenshotsRoot)) {
  walk(screenshotsRoot)
}

const entries = Object.entries(versions).sort(([a], [b]) => a.localeCompare(b))
const body = [
  "// Generated by scripts/generate-screenshot-asset-versions.mjs. Do not edit by hand.",
  "export const screenshotAssetVersions = {",
  ...entries.map(([key, value]) => `  ${JSON.stringify(key)}: ${JSON.stringify(value)},`),
  "} as const",
  "",
].join("\n")

writeFileSync(outputFile, body)
console.log(`Wrote ${entries.length} screenshot versions to ${path.relative(root, outputFile)}`)
